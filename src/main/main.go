package main

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"bytes"
	"fmt"
	"ibd"
	"io"
	"os"
)

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const (
	TestFile = "resource/ibd_checkup_extr.html"
)

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func main() {
	buffer := new(bytes.Buffer)

	f, err := os.Open(TestFile)
	if err != nil {
		panic(err)
	}
	defer f.Close()

	io.Copy(buffer, f)

	info, err := ibd.Parse(buffer)
	if err != nil {
		panic(err)
	}

	fmt.Println(info)
}

