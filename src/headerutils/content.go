package headerutils

//////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"net/http"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func ContentLength(w http.ResponseWriter, length string) {
	w.Header().Add("Content-Length", length)
}

//////////////////////////////////////////////////////////////////////////////////////////////////////

func ContentTypeJsonUTF8(w http.ResponseWriter) {
	w.Header().Add("Content-Type", "application/json;charset=UTF-8")
}

//////////////////////////////////////////////////////////////////////////////////////////////////////

func ContentTypeImageJpeg(w http.ResponseWriter) {
	w.Header().Add("Content-Type", "image/jpeg")
}

//////////////////////////////////////////////////////////////////////////////////////////////////////
