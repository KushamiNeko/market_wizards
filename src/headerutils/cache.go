package headerutils

//////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"fmt"
	"net/http"
	"time"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

const (
	DefaultCache     = 2
	DefaultCacheUnit = time.Hour

	DefaultCacheImages     = 8
	DefaultCacheImagesUnit = time.Hour
)

//////////////////////////////////////////////////////////////////////////////////////////////////////

func CacheExpiresAfter(w http.ResponseWriter, t time.Duration) {
	expires := time.Now().UTC().Add(t).Format(http.TimeFormat)
	cacheControl := fmt.Sprintf("max-age=%d,must-revalidate", int(t.Seconds()))

	w.Header().Add("Expires", expires)
	w.Header().Add("Cache-Control", cacheControl)
}

